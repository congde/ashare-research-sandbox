def chapter_number(path: Path) -> int | None:
    match = re.match(r"^(\d+)-", path.name)
    return int(match.group(1)) if match else None


def heading_title(line: str) -> tuple[int, str] | None:
    match = re.match(r"^(#{2,4}) (.+?)\s*$", line)
    if not match:
        return None
    return len(match.group(1)), match.group(2)


def next_nonempty_index(lines: list[str], start: int) -> int | None:
    for i in range(start, len(lines)):
        if lines[i].strip():
            return i
    return None


def next_heading_index(lines: list[str], start: int) -> int | None:
    for i in range(start, len(lines)):
        if lines[i].lstrip().startswith("#"):
            return i
    return None


def paragraph_before(lines: list[str], index: int) -> str:
    pos = index - 1
    while pos >= 0 and not lines[pos].strip():
        pos -= 1
    parts: list[str] = []
    while pos >= 0 and lines[pos].strip() and not lines[pos].lstrip().startswith("#"):
        parts.insert(0, lines[pos].strip())
        pos -= 1
    return " ".join(parts)


def has_bridge(text: str) -> bool:
    if not text:
        return False
    tail = text[-120:]
    if re.search(r"(?:下面|接下来|下一步|依次|先.{0,12}再|最后一组)", tail):
        return True
    return any(
        phrase in tail
        for phrase in (
            "下面依次",
            "下面三步",
            "下面四",
            "下面用",
            "下面说明",
            "下面固定",
            "下面区分",
            "下面按",
            "案例按四步",
            "流程跑通之后",
            "术语统一之后",
            "路径对照清楚之后",
            "公式可以复算",
            "偏差检查不能替代",
        )
    )


def insert_bridge(lines: list[str], before_index: int, paragraph: str) -> None:
    lines.insert(before_index, "")
    lines.insert(before_index + 1, paragraph)
    lines.insert(before_index + 2, "")


def process_lines(lines: list[str]) -> bool:
    changed = False
    i = 0
    while i < len(lines):
        parsed = heading_title(lines[i])
        if not parsed:
            i += 1
            continue
        level, title = parsed
        nxt = next_heading_index(lines, i + 1)
        if nxt is None:
            i += 1
            continue

        nxt_parsed = heading_title(lines[nxt])
        if not nxt_parsed:
            i += 1
            continue

        if level == 3 and nxt_parsed[0] == 4:
            between_text = " ".join(
                line.strip() for line in lines[i + 1 : nxt] if line.strip()
            )
            intro = H3_INTRO.get(title)
            if intro and not between_text:
                insert_bridge(lines, nxt, intro)
                changed = True
                i = nxt + 1
                continue

        if level == 4 and nxt_parsed[0] == 4:
            bridge = H4_PAIR.get((title, nxt_parsed[1]))
            prev = paragraph_before(lines, nxt)
            if bridge and not has_bridge(prev) and bridge[:24] not in prev:
                insert_bridge(lines, nxt, bridge)
                changed = True
                i = nxt + 1
                continue

        i += 1
    return changed


def process_file(path: Path) -> bool:
    num = chapter_number(path)
    if num is None or num in {0, 1}:
        return False
    text = path.read_text(encoding="utf-8")
    lines = text.splitlines()
    changed = process_lines(lines)
    if changed:
        path.write_text("\n".join(lines) + ("\n" if text.endswith("\n") else ""), encoding="utf-8")
    return changed


def main() -> int:
    updated: list[str] = []
    for path in sorted(CHAPTER_DIR.glob("[0-9][0-9]-*.md")):
        if "修订对照" in path.name:
            continue
        if process_file(path):
            updated.append(path.name)
    if updated:
        print("Added heading transitions in:")
        for name in updated:
            print(f"  - {name}")
    else:
        print("No files needed transition updates.")
    return 0


