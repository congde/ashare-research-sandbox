def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    for ch in CHAPTERS:
        path = OUT / f"{fmt_num(ch['num'])}-{ch['slug']}.md"
        path.write_text(render_chapter(ch), encoding="utf-8")
        print(f"wrote {path.relative_to(ROOT)}")
    for part, name, summary in REVIEWS:
        path = OUT / f"review-part-{part:02d}-{name}.md"
        path.write_text(render_review(part, name, summary), encoding="utf-8")
        print(f"wrote {path.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
