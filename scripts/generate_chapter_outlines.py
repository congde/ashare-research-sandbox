def fmt_num(n: int) -> str:
    return f"{n:02d}"


def render_chapter(ch: dict) -> str:
    n = ch["num"]
    lines = [
        f"# 第 {n} 讲｜{ch['title']}",
        "",
        f"> **写作状态**：大纲（待扩写正文）",
        f"> **所属篇章**：{ch['part']}",
        f"> **上游输入**：{ch['upstream']}",
        f"> **本讲交付**：{ch['deliverable']}",
        f"> **下一讲入口**：{ch['next_entry']}",
        f"> **内容设计**：[出版课程章节内容设计](../出版课程章节内容设计.md)",
        "",
        "## 本讲要讲清的问题",
        "",
        ch["question"],
        "",
        "## 正文大纲",
        "",
    ]
    for i, section in enumerate(ch["sections"], 1):
        lines.extend([f"### {n}.{i} {section}", "", "（待写：现象层 → 原理层 → 方法层 → 案例层 → 证据层）", ""])
    lines.extend(["## 完整示例（大纲）", "", ch["example"], ""])
    lines.extend(["## 图表配置", ""])
    for fig_id, caption, asset in ch["figures"]:
        lines.extend([f"### {fig_id}　{caption}", "", f"- 建议文件：`{asset}`", "- 正文引入：（待写）", "- 解读要点：（待写）", ""])
    for tbl_id, caption in ch["tables"]:
        lines.extend([f"### {tbl_id}　{caption}", "", "（待写表格内容）", ""])
    lines.extend(["## 章节衔接", "", ch["bridge"], ""])
    lines.extend(["## 旧稿复用说明", ""])
    lines.append("| 旧稿位置 | 复用程度 | 说明 |")
    lines.append("|---|---|---|")
    for path, level, note in ch["legacy"]:
        lines.append(f"| `{path}` | {level} | {note} |")
    lines.extend([
        "",
        "## 本章总结",
        "",
        "（待写：本讲改变了什么认识、项目状态发生了什么变化、为什么可以进入下一讲）",
        "",
        "## 练习题",
        "",
        "### 理解题",
        "",
        "1. （待写）",
        "",
        "### 判断题",
        "",
        "1. （待写）",
        "",
        "### 实践题",
        "",
        "1. （待写：与主线项目联动的迷你交付物）",
        "",
    ])
    return "\n".join(lines)


