from __future__ import annotations

import re
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
V2 = ROOT / "docs" / "v2"
LEGACY = V2 / "_legacy"


def read_legacy(name: str) -> str:
    return (LEGACY / name).read_text(encoding="utf-8")


def write_chapter(num: int, slug: str, body: str) -> None:
    path = V2 / f"{num:02d}-{slug}.md"
    path.write_text(body.strip() + "\n", encoding="utf-8")
    chars = len(re.sub(r"\s+", "", body))
    print(f"wrote {path.relative_to(ROOT)} ({chars} chars)")


def renumber(text: str, old: int, new: int) -> str:
    text = re.sub(rf"^# 讲 {old}｜", f"# 第 {new} 讲｜", text, count=1, flags=re.M)
    text = re.sub(rf"^## {old}\.", f"## {new}.", text, flags=re.M)
    text = re.sub(rf"^### {old}\.", f"### {new}.", text, flags=re.M)
    text = re.sub(rf"图 {old}-", f"图 {new}-", text)
    text = re.sub(rf"表 {old}-", f"表 {new}-", text)
    text = re.sub(rf"chapter-{old:02d}-", f"chapter-{new:02d}-", text)
    text = re.sub(rf"第 {old} 讲", f"第 {new} 讲", text)
    return text


