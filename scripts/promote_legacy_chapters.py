def ensure_hooks(text: str, prior: str, nxt: str) -> str:
    lines = text.splitlines()
    if not re.search(r"上一讲|前几讲|第[一二三四五六七八九十]+讲", "\n".join(lines[:40])):
        insert = f"\n{prior}\n"
        for i, line in enumerate(lines):
            if line.startswith("## "):
                lines.insert(i, insert.strip())
                break
        text = "\n".join(lines)
    if "下一讲" not in "\n".join(lines[-100:]):
        text = text.rstrip() + f"\n\n下一讲{nxt}。\n"
    return text


